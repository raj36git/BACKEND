// \n, \t, \b, ...
var text = "I am from Nepal \n Currently studying in India"
console.log(text);

//print \ in between text
var text2 = "I am from Nepal \\ Currently studying in India"
console.log(text2);

//print space as tab
var text3 = "I am from Nepal \t Currently studying in India"
console.log(text3);

//print backspace
var text4 = "I am from Nepal \b    Currently studying in India"
console.log(text4);